coisa = input('Digite alguma coisa: ')

print('O seu tipo é:', type(coisa))
print('Tem espaço?',coisa.isspace())
print('É número?', coisa.isnumeric())
print('É alfabeto?', coisa.isalpha())
print('É alfanúmerico?', coisa.isalpha())
print('Ésta em maiúsculo?',coisa.isupper())
print('Ésta em minusculo?',coisa.islower())



